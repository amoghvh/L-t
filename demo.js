//data types

//1.string
let name = "amogh";
console.log(name);
console.log(typeof name);

//2.number
let age = 21;
console.log(age);
console.log(typeof age); 

//3.boolean
let isStudent = true;
console.log(isStudent);
console.log(typeof(isStudent));

//4.undefined
let address;
console.log(address);
console.log(typeof address);

//5.null
let city = null;
console.log(city);
console.log(typeof city);

//6.symbol
let id = Symbol("id");  
console.log(id);
console.log(typeof id);

//7.object

let student = {
    student_name:"amogh",
    age: 20
};
console.log(student);
console.log(typeof student);

//8.array
let arr = ["css", "it", "iot"];
console.log(arr);
console.log(typeof arr);